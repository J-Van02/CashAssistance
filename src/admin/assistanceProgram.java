    private void dash3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dash3MouseClicked
    int rowindex = progtbl.getSelectedRow();
        if(rowindex < 0){
            JOptionPane.showMessageDialog(null, "Please Select an item!");
        } else {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this program?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                dbConnector dbc = new dbConnector();
                TableModel model = progtbl.getModel();
                String id = model.getValueAt(rowindex, 0).toString();
                dbc.updateData("DELETE FROM tbl_progran WHERE prog_id = '" + id + "'");
                Session sess = Session.getInstance();
                dbc.logActivity(sess.getUid(), "Deleted program ID: " + id);
                displayprograms();
                JOptionPane.showMessageDialog(null, "Program deleted successfully!");
            }
        }
    }//GEN-LAST:event_dash3MouseClicked

