    private void dash4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dash4MouseClicked
    int rowIndex = progtbl.getSelectedRow();
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null,"Please Select an Item!");
        }else{
            try{

                dbConnector dbc = new dbConnector();
                TableModel tbl = progtbl.getModel();
                ResultSet rst = dbc.getData("SELECT * FROM tbl_program WHERE prog_id = '"+tbl.getValueAt(rowIndex, 0)+"'");
                if(rst.next()){
                    viewAssistanceProgram vap = new viewAssistanceProgram();
                    vap.progid.setText(""+rst.getInt("prog_id"));
                    vap.progname.setText(""+rst.getString("prog_name"));
                    vap.progdes.setText(""+rst.getString("prog_description"));
                    vap.applimit.setText(""+rst.getString("prog_applicantLimit"));
                    vap.totalfunds.setText(""+rst.getString("prog_allocatedFund"));
                    vap.setVisible(true);
                    this.dispose();
                }
            }catch(SQLException ex){
                System.out.println(""+ex);
            }
        }
    }//GEN-LAST:event_dash4MouseClicked

