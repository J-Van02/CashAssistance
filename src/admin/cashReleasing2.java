    private void cnlMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cnlMouseClicked
    String releaseCash = cash.getText().trim();
    String selectedApplicant = appname.getSelectedItem().toString();
    String selectedProgram = progname.getSelectedItem().toString();

    if (releaseCash.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Release Cash field is required!", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!releaseCash.matches("^\\d+(\\.\\d{1,2})?$")) { 
        JOptionPane.showMessageDialog(this, "Release cash should be a valid number!", "Error", JOptionPane.ERROR_MESSAGE);
        cash.setText("");
    } else if ("Please Select Applicant".equals(selectedApplicant) || "N/A".equals(selectedApplicant)) {
        JOptionPane.showMessageDialog(this, "Please select a valid applicant", "Error", JOptionPane.ERROR_MESSAGE);
    } else if ("Please Select Program".equals(selectedProgram) || "N/A".equals(selectedProgram)) { 
        JOptionPane.showMessageDialog(this, "Please select a valid program", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        dbConnector db = new dbConnector();
        Integer userId = applicantMap.get(selectedApplicant);
        Integer progId = programtMap.get(selectedProgram);

        String insertQuery = "INSERT INTO tbl_cashreleasing (cash_appid, cash_release, cash_progid) VALUES (?, ?, ?)";
        try (PreparedStatement pstmt = db.connect.prepareStatement(insertQuery)) {
            pstmt.setInt(1, userId);
            pstmt.setString(2, releaseCash);
            pstmt.setInt(3, progId);

            if (pstmt.executeUpdate() == 1) {
                Session sess = Session.getInstance();
                db.logActivity(sess.getUid(), "Released Cash Assistance to: " + userId);
                JOptionPane.showMessageDialog(this, "Release Cash Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                cashReleasing ef = new cashReleasing();
                ef.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to release cash");
            }
        } catch (SQLException ex) {
            System.out.println(""+ex);
            JOptionPane.showMessageDialog(null, "Database error occurred");
        }
    }
    }//GEN-LAST:event_cnlMouseClicked

