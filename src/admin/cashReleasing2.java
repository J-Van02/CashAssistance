    public void loadProgram(){
        dbConnector db = new dbConnector();
        try {
            String query = "SELECT prog_id, prog_name FROM tbl_program ORDER BY prog_name";
            ResultSet rs = db.getData(query);

            progname.removeAllItems();
            programtMap.clear(); 
            boolean hasProgram = false;

            while (rs.next()) {
                    hasProgram = true;
                    int progId = rs.getInt("prog_id");
                    String progName = rs.getString("prog_name");
                    progname.addItem(progName);
                    programtMap.put(progName, progId);
            }

            progname.revalidate();
            progname.repaint();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading program: " + e.getMessage());
        }
    }
    
    private void loadApplicant() {
    try {
        dbConnector db = new dbConnector();

        String query = "SELECT a.app_id, u.u_fname, u.u_lname " +  
               "FROM tbl_user u " +
               "INNER JOIN tbl_applicant a ON u.u_id = a.app_uid " +  
               "WHERE u.u_type = 'Applicant' " +
               "AND a.app_status = 'Accepted' " +
               "AND a.app_id NOT IN (SELECT cash_appid FROM tbl_cashreleasing) " +  
               "ORDER BY u.u_fname";
        
        ResultSet rs = db.getData(query);

        appname.removeAllItems();
        applicantMap.clear();

        boolean hasApplicants = false;
        while (rs.next()) {
            hasApplicants = true;
            int applicantId = rs.getInt("app_id");
            String fullName = rs.getString("u_fname") + " " + rs.getString("u_lname");
            appname.addItem(fullName);
            applicantMap.put(fullName, applicantId);
        }

        rs.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, 
            "Error loading applicants: " + e.getMessage(),
            "Database Error",
            JOptionPane.ERROR_MESSAGE);
    }
}
    
